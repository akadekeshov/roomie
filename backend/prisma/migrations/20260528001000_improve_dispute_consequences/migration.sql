DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'DisputeDecision') THEN
    CREATE TYPE "DisputeDecision" AS ENUM ('NONE', 'ACCEPTED', 'REJECTED', 'NEED_MORE_INFO');
  END IF;
END
$$;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'DisputeAction') THEN
    CREATE TYPE "DisputeAction" AS ENUM (
      'NONE',
      'WARNING',
      'TEMPORARY_RESTRICTION',
      'ACCOUNT_BAN',
      'AGREEMENT_CANCELLED',
      'PAYMENT_REQUIRED',
      'PROFILE_FLAGGED'
    );
  END IF;
END
$$;

ALTER TABLE "users"
  ADD COLUMN IF NOT EXISTS "warningCount" INTEGER NOT NULL DEFAULT 0,
  ADD COLUMN IF NOT EXISTS "isRestricted" BOOLEAN NOT NULL DEFAULT false,
  ADD COLUMN IF NOT EXISTS "restrictedUntil" TIMESTAMP(3),
  ADD COLUMN IF NOT EXISTS "bannedAt" TIMESTAMP(3),
  ADD COLUMN IF NOT EXISTS "banReason" TEXT,
  ADD COLUMN IF NOT EXISTS "profileTrustPenalty" INTEGER NOT NULL DEFAULT 0;

ALTER TABLE "disputes"
  ADD COLUMN IF NOT EXISTS "decision" "DisputeDecision" NOT NULL DEFAULT 'NONE',
  ADD COLUMN IF NOT EXISTS "action" "DisputeAction" NOT NULL DEFAULT 'NONE',
  ADD COLUMN IF NOT EXISTS "resultText" TEXT,
  ADD COLUMN IF NOT EXISTS "actionAppliedAt" TIMESTAMP(3),
  ADD COLUMN IF NOT EXISTS "actionExpiresAt" TIMESTAMP(3),
  ADD COLUMN IF NOT EXISTS "accusedNotifiedAt" TIMESTAMP(3),
  ADD COLUMN IF NOT EXISTS "reporterNotifiedAt" TIMESTAMP(3);
